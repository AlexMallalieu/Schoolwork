num1 = float(input("Enter num1:"))
num2 = float(input("Enter num2:"))

if num1 > num2:
    print(str(num1) + " is greater than " + str(num2))
elif num1 < num2:
    print(str(num1) + " is less than " + str(num2))
elif num1 == num2:
    print(str(num1) + " is equal to " + str(num2))


